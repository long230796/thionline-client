package thionline.entities.dethi;

public class Traloi {
	String cauhoi;
	String dapan;
	public Traloi(String cauhoi, String dapan) {
		super();
		this.cauhoi = cauhoi;
		this.dapan = dapan;
	}
	public String getCauhoi() {
		return cauhoi;
	}
	public void setCauhoi(String cauhoi) {
		this.cauhoi = cauhoi;
	}
	public String getDapan() {
		return dapan;
	}
	public void setDapan(String dapan) {
		this.dapan = dapan;
	}
	
	
	
	
}
